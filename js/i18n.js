let currentLang = 'pt';

export function t(key) { return i18n[currentLang][key] ?? key; }
export function getLang() { return currentLang; }
export function setLang(l) { currentLang = l; }
export function getVerified() { return i18n[currentLang].verified; }

export function decSep() { return currentLang === 'pt' ? ',' : '.'; }
export function locale() { return currentLang === 'pt' ? 'pt-BR' : 'en-US'; }
export function nFmt(n, d = 2) { return n.toFixed(d).replace('.', decSep()); }

export function fmt(mnok, decM = 2) {
  if (Math.abs(mnok) >= 1) return nFmt(mnok, decM) + ' MNOK';
  return Math.round(mnok * 1000).toLocaleString(locale()) + ' KNOK';
}

export function fmtM(mnok, d = 2) { return nFmt(mnok, d) + ' MNOK'; }
export function fmtK(mnok) { return Math.round(mnok * 1000).toLocaleString(locale()) + ' KNOK'; }
export function fmtSign(mnok) { return (mnok >= 0 ? '+' : '−') + fmt(Math.abs(mnok)); }
export function pct(v, d = 1) { return nFmt(v, d) + '%'; }
